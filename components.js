class Position {
	constructor(x, y) {
		this.x = x;
		this.y = y;
	}
}

class Velocity {
	constructor(x, y) {
		this.x = x;
		this.y = y;
	}
}

class Size {
	constructor(x, y) {
		this.x = x;
		this.y = y;
	}
}

class Gravity {
	constructor(value) {
		this.g = value;
	}
}

class Weight {
	constructor(weight) {
		this.weight = weight;
	}
}

class LifeSpan {
	constructor(endTime) {
		this.life = 0;
		this.endTime = endTime;
	}
}

class Color {
	constructor(r, g, b) {
		this.r = r;
		this.g = g;
		this.b = b;
	}
}